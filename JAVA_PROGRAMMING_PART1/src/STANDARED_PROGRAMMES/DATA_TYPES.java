package STANDARED_PROGRAMMES;

public class DATA_TYPES 
{

	public static void main(String[] args) 
	{
		String name = "Siva"; //STRING (multiple characters)
		System.out.println(name);
		int age = 22; //INTEGER (int)
		System.out.println(age);
		char firstLetter = 'S'; //SINGLE CHARACTER (char)
		System.out.println(firstLetter);
		System.out.println("Are you ready to relocate?");
		boolean relocate = true; //BOOLEAN
		System.out.println(relocate);
		String mailId = "e.siva306@gmail.com"; //SRTING (multiple characters)
		System.out.println(mailId);
		double graduatePercentage = 68.9; //DECIMALS (double)
		System.out.println(graduatePercentage);
		long cellNumber = 8519831700l; //INTEGER (long)
		System.out.println(cellNumber);
	}

}