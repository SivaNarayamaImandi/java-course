package MajorOperatorsInJava;

public class ConditionalOperators 
{
	public static void main(String[] args) 
	{
		// 5. Conditional Operators
		  
		  int a = 5;
		  int b = (a<10)?50:100;
		  System.out.println("b value is : "+b);
		  
		  int c = 10;		  
		  int d = (c>15)?10:15;
		  System.out.println("d value is : "+d);

	}

}